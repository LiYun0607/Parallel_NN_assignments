n_cities = 4
bel = 1000
w = np.zeros([n_cities ** 2, n_cities ** 2])
theta = np.zeros(n_cities ** 2)
time_step = 100000

mat_d = np.array([
    [0, 85, 114, 65],
    [85, 0, 89, 113],
    [114, 89, 0, 86],
    [65, 113, 86, 0],
])


def energy_function(x_0):
    energy = 0
    for i in range(n_cities):
        for j in range(n_cities):
            for k in range(n_cities):
                energy += x_0[i][j] * x_0[mod(i+1, 4)][k] * mat_d[j][k]
    e = 0
    for i in range(n_cities):
        x_ = 0
        x_2 = 0
        for j in range(n_cities):
            x_ += x_0[i][j]
            x_2 += x_0[j][i]
        e += (x_ - 1) ** 2
        e += (x_2 - 1) ** 2
    energy += bel * e

    return energy


c = energy_function(np.zeros([n_cities, n_cities]))
# get theta
for i in range(n_cities):
    for j in range(n_cities):
        mat = np.zeros([n_cities, n_cities])
        mat[i][j] = 1
        theta[i * n_cities + j] = energy_function(mat) - c

# get weights of the CNN network
for i in range(n_cities):
    for j in range(n_cities):
        for k in range(n_cities):
            for m in range(n_cities):
                mat = np.zeros([n_cities ** 2, n_cities ** 2])
                mat[i][j] = 1
                mat[k][m] = 1
                w[i * n_cities + j][k * n_cities + m] = -energy_function(mat) + \
                                                        theta[i * n_cities + j] + theta[k * n_cities + m] + c
                if i * n_cities + j == k * n_cities + m:
                    w[i * n_cities + j][k * n_cities + m] = 0

print(f"weights: {w}; c: {c}, theta: {theta}")


